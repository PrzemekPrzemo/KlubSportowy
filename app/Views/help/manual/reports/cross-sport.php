<p class="lead">Cross-sport statystyki pokazują kondycję klubu w wielu wymiarach: ilu członków trenuje w kilku sekcjach, jaka jest ich frekwencja per sport, jakie są wyniki. Pomaga identyfikować zawodników wszechstronnych i sport-flagship klubu.</p>

<h2>Krok po kroku</h2>
<ol>
    <li>Wejdź w <strong>Raporty → Cross-sport</strong>.</li>
    <li>Wybierz okres analizy (miesiąc, kwartał, rok).</li>
    <li>Zobacz tabele: liczba zawodników na sport, średnia frekwencja, liczba wyników w turniejach, suma składek.</li>
    <li>Diagram Venn pokazuje overlap — ile osób trenuje w 2, 3, 4 sekcjach jednocześnie.</li>
    <li>Drill-down: kliknij segment aby zobaczyć listę konkretnych zawodników.</li>
</ol>

<div class="manual-callout manual-callout-tip">
    <strong><i class="bi bi-lightbulb"></i> Wskaźnik wszechstronności.</strong> Klub z >20% członków trenujących w 2+ sekcjach jest oceniany jako „wszechstronny" — atrakcyjny dla rodziców szukających rozwoju ogólnego dziecka.
</div>


<p class="lead">System poleceń i rabatów pozwala nagradzać członków za promowanie klubu wśród znajomych. Każdy członek otrzymuje unikalny link polecający — gdy ktoś dołączy przez ten link, obaj otrzymują rabat lub bonus.</p>

<h2>Krok po kroku</h2>
<ol>
    <li>Włącz program w <strong>Marketing → Polecenia → Konfiguracja</strong>.</li>
    <li>Określ nagrodę: <em>rabat % na następną składkę</em>, <em>kwota zł</em>, <em>darmowy miesiąc</em>, <em>upominek klubowy</em>.</li>
    <li>Określ próg uruchomienia nagrody (np. polecony musi opłacić pierwszą składkę).</li>
    <li>Członkowie zobaczą swój link polecający w portalu w sekcji <em>Polecaj klub</em>.</li>
    <li>Statystyki — ile osób poleciło, ile się zarejestrowało, ile przyniosło przychodu — widoczne w <em>Marketing → Polecenia → Statystyki</em>.</li>
</ol>

<div class="manual-callout manual-callout-tip">
    <strong><i class="bi bi-lightbulb"></i> Kampanie sezonowe.</strong> Uruchamiaj kampanie czasowe (np. „Lato 2026: poleć znajomego, oboje macie 2 miesiące za 1 zł") — zwiększają konwersję o 30-50%.
</div>

<h2>Najczęstsze pytania</h2>
<div class="manual-faq">
    <details>
        <summary>Czy mogę ograniczyć liczbę poleceń?</summary>
        <div class="faq-body">Tak — np. max 5 wykorzystanych poleceń na członka, lub max 10 osób przez kod.</div>
    </details>
    <details>
        <summary>Co z rabatami stałymi (np. lojalność)?</summary>
        <div class="faq-body">To osobny mechanizm w <em>Finanse → Rabaty</em> — np. -5% po 3 latach członkostwa.</div>
    </details>
    <details>
        <summary>Czy widać kto kogo polecił?</summary>
        <div class="faq-body">Tak, ale tylko administrator. Zachowuje to przejrzystość programu.</div>
    </details>
</div>

<p class="lead">Prowizje trenerów to elastyczny system rozliczeń pracy szkoleniowej — trener może otrzymywać stałą pensję, prowizję od liczby zawodników, prowizję od opłaconych składek, prowizję od wyników sportowych lub kombinację powyższych.</p>

<h2>Krok po kroku</h2>
<ol>
    <li>Wejdź w <strong>Finanse → Prowizje → Reguły</strong>.</li>
    <li>Kliknij <em>+ Nowa reguła</em> i przypisz do konkretnego trenera (lub grupy trenerów).</li>
    <li>Wybierz typ: <em>% od składek</em>, <em>kwota za zawodnika</em>, <em>kwota za trening</em>, <em>premia za wynik</em>.</li>
    <li>Określ wartość (np. 15% od opłaconych składek sekcji „Piłka nożna — Juniorzy").</li>
    <li>Ustaw okres rozliczeniowy (miesiąc, kwartał).</li>
    <li>Po zakończeniu okresu system wygeneruje automatycznie rozliczenie i propozycję wypłaty.</li>
</ol>

<div class="manual-callout manual-callout-tip">
    <strong><i class="bi bi-lightbulb"></i> B2B vs UoP.</strong> Dla trenerów na umowie B2B system generuje noty obciążeniowe gotowe do faktury własnej. Dla umów o pracę — raport pomocniczy do listy płac.
</div>

<h2>Najczęstsze pytania</h2>
<div class="manual-faq">
    <details>
        <summary>Czy mogę łączyć reguły?</summary>
        <div class="faq-body">Tak — np. „pensja podstawowa 2000 zł + 10% od składek powyżej progu". System sumuje wszystkie aktywne reguły.</div>
    </details>
    <details>
        <summary>Co jeśli zawodnik nie zapłacił składki?</summary>
        <div class="faq-body">Prowizja od składek liczona jest TYLKO od opłaconych — nie generuje pretensji.</div>
    </details>
    <details>
        <summary>Czy trener widzi swoją prowizję?</summary>
        <div class="faq-body">Tak — w portalu trenera w zakładce <em>Moje wynagrodzenia</em>.</div>
    </details>
</div>

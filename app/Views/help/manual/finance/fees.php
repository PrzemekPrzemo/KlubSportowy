<p class="lead">Składki członkowskie to główne źródło przychodów klubu. ClubDesk pozwala konfigurować elastyczne stawki per sekcja, kategoria wiekowa i okres, z możliwością rabatów rodzinnych i promocji.</p>

<h2>Krok po kroku</h2>
<ol>
    <li>Otwórz <strong>Finanse → Składki → Konfiguracja</strong>.</li>
    <li>Wybierz sekcję i kategorię wiekową.</li>
    <li>Ustaw kwotę miesięczną, kwartalną lub roczną (możesz mieć kilka wariantów).</li>
    <li>Skonfiguruj termin płatności (np. do 10. każdego miesiąca) i tolerancję (ile dni grace period).</li>
    <li>Dodaj rabaty: rodzinny (2. dziecko 50%, 3. — 100%), uczniowski, dla seniorów, lojalnościowy.</li>
    <li>Zapisz — system zacznie generować naliczenia automatycznie.</li>
</ol>

<div class="manual-callout manual-callout-tip">
    <strong><i class="bi bi-lightbulb"></i> Składki proporcjonalne.</strong> Włącz <em>Składki proporcjonalne</em> dla członków dołączających w trakcie miesiąca — system wyliczy kwotę za dni faktycznego członkostwa.
</div>

<h2>Najczęstsze pytania</h2>
<div class="manual-faq">
    <details>
        <summary>Czy mogę mieć różne stawki dla tego samego zawodnika?</summary>
        <div class="faq-body">Tak — np. składka treningowa + składka licencyjna + składka turniejowa są naliczane osobno.</div>
    </details>
    <details>
        <summary>Co z osobami, które nie trenują w danym miesiącu?</summary>
        <div class="faq-body">Możesz oznaczyć <em>Zawieszenie składek</em> — np. dla kontuzjowanych. Naliczenie zostanie wstrzymane.</div>
    </details>
    <details>
        <summary>Jak działa rabat rodzinny?</summary>
        <div class="faq-body">System wykrywa rodzeństwo na podstawie powiązań opiekunów. Konfigurowalne progi — 2. dziecko, 3. dziecko, ≥4.</div>
    </details>
</div>

<p class="lead">Ogłoszenia to oficjalna forma komunikacji klubu z członkami. Inaczej niż e-mail (jednorazowy), ogłoszenie pozostaje widoczne w portalu, ma priorytet, targetowanie i statystyki odczytów.</p>

<h2>Krok po kroku</h2>
<ol>
    <li>Otwórz <strong>Komunikacja → Ogłoszenia → + Nowe</strong>.</li>
    <li>Wpisz tytuł i treść (edytor WYSIWYG z formatowaniem, obrazkami, linkami).</li>
    <li>Wybierz priorytet: <em>Informacyjny</em> (szary), <em>Ważny</em> (niebieski), <em>Pilny</em> (czerwony, z pop-upem).</li>
    <li>Targetowanie: cały klub, konkretne sekcje, grupa wiekowa, członkowie z zaległościami, etc.</li>
    <li>Określ datę publikacji i (opcjonalnie) datę wygaśnięcia.</li>
    <li>Wybierz kanały: portal, push notification, e-mail.</li>
    <li>Kliknij <strong>Opublikuj</strong>.</li>
</ol>

<div class="manual-callout manual-callout-tip">
    <strong><i class="bi bi-lightbulb"></i> Statystyki odczytów.</strong> Każde ogłoszenie ma statystyki: ile osób przeczytało, ile kliknęło link, ile czasu poświęciło. Wykorzystuj do oceny efektywności komunikacji.
</div>

<h2>Najczęstsze pytania</h2>
<div class="manual-faq">
    <details>
        <summary>Czy ogłoszenia można edytować po publikacji?</summary>
        <div class="faq-body">Tak, ale w audit logu zostaje ślad pierwotnej wersji. Wprowadź adnotację „[Edytowano DD-MM-RRRR]" przy istotnych zmianach.</div>
    </details>
    <details>
        <summary>Jaki limit znaków?</summary>
        <div class="faq-body">Brak — w portalu używamy paginacji dla długich treści. Dla push: skróty do 200 znaków automatycznie.</div>
    </details>
    <details>
        <summary>Czy mogę zaplanować serię?</summary>
        <div class="faq-body">Tak — np. cotygodniowe podsumowania. Konfiguracja w <em>Szablony cykliczne</em>.</div>
    </details>
</div>

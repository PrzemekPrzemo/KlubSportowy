<p class="lead">Drabinka turniejowa wizualizuje rozgrywki w czytelnej formie. ClubDesk generuje interaktywne drabinki dla wszystkich popularnych formatów i automatycznie aktualizuje je po wpisaniu wyniku.</p>

<h2>Krok po kroku</h2>
<ol>
    <li>Wybierz turniej i zakładkę <strong>Drabinka</strong>.</li>
    <li>Dla single elimination — system rozstawia uczestników wg seedingu (rankingowo) lub losowo.</li>
    <li>Dla double elimination — drabinka zwycięzców (winners bracket) i przegranych (losers bracket).</li>
    <li>Dla round-robin — tabela ligowa z punktacją i bezpośrednimi pojedynkami.</li>
    <li>Kliknij na mecz w drabince aby wprowadzić wynik.</li>
    <li>Po każdym meczu drabinka aktualizuje się — zwycięzca awansuje, przegrany trafia do losers bracket (jeśli double elim).</li>
</ol>


<h2>Najczęstsze pytania</h2>
<div class="manual-faq">
    <details>
        <summary>Co to seeding?</summary>
        <div class="faq-body">Rozstawianie najlepszych zawodników tak, aby spotkali się dopiero w późniejszych rundach. System używa rankingu klubu lub federacji.</div>
    </details>
    <details>
        <summary>Czy mogę ręcznie ustawić rozstawienie?</summary>
        <div class="faq-body">Tak — w widoku drabinki przed startem turnieju przeciągnij uczestników między slotami (drag & drop).</div>
    </details>
    <details>
        <summary>Co jeśli zawodnik się wycofa?</summary>
        <div class="faq-body">Edytuj uczestnika → <em>Walkower</em>. Przeciwnik awansuje automatycznie.</div>
    </details>
</div>

<p class="lead">Moduł turniejów obsługuje rozgrywki single elimination, double elimination, round-robin i ligowe — z automatycznym generowaniem terminarza, harmonogramem meczów i listą uczestników.</p>

<h2>Krok po kroku</h2>
<ol>
    <li>Wejdź w <strong>Sport → Turnieje → + Nowy turniej</strong>.</li>
    <li>Wpisz nazwę, sekcję, kategorię, daty (start, koniec).</li>
    <li>Wybierz format: <em>Single elimination</em>, <em>Double elimination</em>, <em>Round-robin</em>, <em>Mieszany (grupy + pucharowa)</em>.</li>
    <li>Dodaj uczestników: wybierz z listy zawodników/drużyn klubu lub dodaj zewnętrznych (z innych klubów).</li>
    <li>Określ harmonogram meczów (lub pozwól systemowi wygenerować automatycznie).</li>
    <li>Zapisz — system wygeneruje drabinkę i opublikuje turniej w portalu członka.</li>
</ol>

<div class="manual-callout manual-callout-tip">
    <strong><i class="bi bi-lightbulb"></i> Turnieje otwarte.</strong> Włączając opcję <em>Turniej publiczny</em>, kibice mogą śledzić wyniki na żywo bez logowania (link <code>app.clubdesk.pl/tournaments/{slug}</code>).
</div>

<h2>Najczęstsze pytania</h2>
<div class="manual-faq">
    <details>
        <summary>Ilu uczestników max?</summary>
        <div class="faq-body">Single/double elimination — do 256 (8 rund). Round-robin — do 32 (praktyczny limit czasowy).</div>
    </details>
    <details>
        <summary>Czy mogę zaprosić zewnętrznych?</summary>
        <div class="faq-body">Tak — dodaj jako gościa z imieniem, nazwiskiem i klubem reprezentowanym.</div>
    </details>
    <details>
        <summary>Czy turniej rozliczany jest finansowo?</summary>
        <div class="faq-body">Opcjonalnie — możesz włączyć wpisowe (przez bramkę płatności) i automatyczne wypłaty dla zwycięzców.</div>
    </details>
</div>

<p class="lead">Plan zajęć cyklicznych pozwala raz zdefiniować harmonogram treningów (np. „Pon 18:00-20:00, Śr 18:00-20:00") i system automatycznie generuje pojedyncze sesje na cały sezon.</p>

<h2>Krok po kroku</h2>
<ol>
    <li>Przejdź do <strong>Sport → Plan zajęć</strong>.</li>
    <li>Kliknij <em>+ Nowy harmonogram</em> i wybierz sekcję.</li>
    <li>Określ wzorzec powtarzania: dni tygodnia, godzina rozpoczęcia, czas trwania.</li>
    <li>Wskaż miejsce treningu (hala, boisko zewnętrzne, sala konferencyjna).</li>
    <li>Wybierz datę startu i zakończenia generowania (max 12 miesięcy w przyszłość).</li>
    <li>System wygeneruje sesje i zapisze je w kalendarzu — możesz pojedyncze odwołać lub przesunąć.</li>
</ol>

<div class="manual-callout manual-callout-tip">
    <strong><i class="bi bi-lightbulb"></i> Wakacje i przerwy.</strong> W ustawieniach klubu zdefiniuj okresy bez treningów (np. ferie, święta) — generator automatycznie je pominie.
</div>

<h2>Najczęstsze pytania</h2>
<div class="manual-faq">
    <details>
        <summary>Co jeśli zmienia się stała godzina?</summary>
        <div class="faq-body">Edytuj harmonogram → system zapyta, czy zmiana ma dotyczyć tylko przyszłych sesji, czy także już zaplanowanych.</div>
    </details>
    <details>
        <summary>Czy zawodnicy widzą plan?</summary>
        <div class="faq-body">Tak — w portalu w zakładce <em>Mój kalendarz</em>. Mogą też wyeksportować do Google Calendar / iCal.</div>
    </details>
    <details>
        <summary>Czy mogę dodać wyjątek (np. „w tym tygodniu trening odwołany")?</summary>
        <div class="faq-body">Tak — kliknij konkretną sesję w kalendarzu → <em>Odwołaj</em>. Zawodnicy otrzymają powiadomienie.</div>
    </details>
</div>

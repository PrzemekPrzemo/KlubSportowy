<p class="lead">Sekcja sportowa to organizacyjny rdzeń klubu — grupuje zawodników, trenerów, treningi, składki i wyniki. W tej sekcji omówimy zarządzanie sekcjami i przypisywanie trenerów.</p>

<h2>Krok po kroku</h2>
<ol>
    <li>Otwórz <strong>Klub → Sekcje sportowe</strong>.</li>
    <li>Kliknij sekcję, którą chcesz edytować (lub <em>+ Dodaj</em>).</li>
    <li>W zakładce <em>Trenerzy</em> przypisz jednego głównego trenera i (opcjonalnie) asystentów / instruktorów.</li>
    <li>Ustaw kategorie wiekowe (np. U-10, U-12, U-14, Senior) — każda może mieć osobne stawki składek.</li>
    <li>Zdefiniuj sezon: data startu, data końca, terminy startów licencyjnych.</li>
    <li>Zapisz zmiany — propagują się natychmiast do listy członków i kalendarza.</li>
</ol>

<div class="manual-callout manual-callout-tip">
    <strong><i class="bi bi-lightbulb"></i> Trenerzy a uprawnienia.</strong> Trener przypisany do sekcji widzi wyłącznie zawodników tej sekcji. Aby dać dostęp do całego klubu, przypisz rolę <em>dyrektor sportowy</em> w <em>Ustawienia → Role</em>.
</div>

<h2>Najczęstsze pytania</h2>
<div class="manual-faq">
    <details>
        <summary>Czy mogę mieć trenera w wielu sekcjach?</summary>
        <div class="faq-body">Tak — bez limitu. Każda sekcja jest osobnym przypisaniem.</div>
    </details>
    <details>
        <summary>Co jeśli trener odchodzi?</summary>
        <div class="faq-body">Zarchiwizuj jego przypisanie (zachowa historię). System poprosi o wskazanie zastępcy lub pozostawienie sekcji bez trenera czasowo.</div>
    </details>
    <details>
        <summary>Jak archiwizować sekcję?</summary>
        <div class="faq-body">Edycja sekcji → <em>Status → Archiwum</em>. Zawodnicy nie tracą historii, ale sekcja przestaje pojawiać się w aktywnych widokach.</div>
    </details>
</div>

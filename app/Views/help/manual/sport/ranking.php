<p class="lead">Cross-sport ranking pozwala śledzić wszechstronność zawodników trenujących w wielu sekcjach (np. piłka nożna + lekkoatletyka). System sumuje punkty z różnych dyscyplin według konfigurowalnych wag.</p>

<h2>Krok po kroku</h2>
<ol>
    <li>Otwórz <strong>Sport → Ranking cross-sport</strong>.</li>
    <li>W ustawieniach (<em>Konfiguracja</em>) ustaw wagi dla każdej sekcji — domyślnie 1.0, możesz zwiększyć dla sportów priorytetowych klubu.</li>
    <li>Wybierz okres rankingu (miesiąc, kwartał, sezon, rok).</li>
    <li>System wyświetla tabelę zawodników z punktami z każdej sekcji i sumą ważoną.</li>
    <li>Top 10 jest automatycznie publikowane w portalu członka (jeśli włączone).</li>
</ol>

<div class="manual-callout manual-callout-tip">
    <strong><i class="bi bi-lightbulb"></i> Wszechstronność.</strong> Włącz mnożnik <em>bonus za sport drugi</em> aby premiować zawodników trenujących wielodyscyplinarnie — buduje to kulturę wszechstronności w klubie.
</div>

<h2>Najczęstsze pytania</h2>
<div class="manual-faq">
    <details>
        <summary>Skąd biorą się punkty?</summary>
        <div class="faq-body">Z wyników turniejów (1 miejsce = X pkt, 2 = Y itd.), frekwencji na treningach i osiągnięć indywidualnych. Konfiguracja w <em>Sport → Ranking → Punktacja</em>.</div>
    </details>
    <details>
        <summary>Czy mogę wyłączyć ranking dla sekcji?</summary>
        <div class="faq-body">Tak — w edycji sekcji ustaw <em>Wliczaj do cross-sport rankingu: NIE</em>.</div>
    </details>
    <details>
        <summary>Czy publikowany jest pełny ranking, czy tylko top X?</summary>
        <div class="faq-body">Konfigurowalne — domyślnie top 20 publicznie, pełny tylko dla administratorów.</div>
    </details>
</div>

<p class="lead">W ClubDesk występują dwa pokrewne, ale różne pojęcia: <strong>członek klubu</strong> i <strong>zawodnik</strong>. Zrozumienie różnicy jest kluczowe dla prawidłowej konfiguracji składek, list startowych i raportów.</p>

<h2>Krok po kroku</h2>
<ol>
    <li><strong>Członek</strong> to każda osoba zarejestrowana w klubie — także rodzice, kibice klubowi, sponsorzy, członkowie honorowi, byli zawodnicy.</li>
    <li><strong>Zawodnik</strong> to członek z przypisaniem do co najmniej jednej sekcji sportowej i aktywnym statusem trenowania (kategoria, klasa, licencja).</li>
    <li>Każdy zawodnik jest członkiem, ale nie każdy członek jest zawodnikiem.</li>
    <li>Składki członkowskie (np. wpisowe roczne) mogą dotyczyć wszystkich członków, natomiast składki treningowe — tylko zawodników.</li>
    <li>W raportach „Wszyscy członkowie" widzisz pełną bazę osób, w raporcie „Zawodnicy" — wyłącznie tych z aktywną sekcją.</li>
</ol>

<div class="manual-callout manual-callout-tip">
    <strong><i class="bi bi-lightbulb"></i> Filtry.</strong> W liście członków używaj filtra <em>Typ: Tylko zawodnicy</em>, aby wykluczyć członków honorowych i opiekunów z masowych operacji finansowych.
</div>

<h2>Najczęstsze pytania</h2>
<div class="manual-faq">
    <details>
        <summary>Czy opiekun (rodzic) liczy się do limitu planu?</summary>
        <div class="faq-body">Nie. Konta opiekunów są bezpłatne i nie wliczają się do limitu członków planu. Liczą się wyłącznie konta typu „członek/zawodnik".</div>
    </details>
    <details>
        <summary>Jak zrobić z zawodnika członka honorowego po zakończeniu kariery?</summary>
        <div class="faq-body">Edytuj profil → zakładka <em>Sekcje</em> → zarchiwizuj wszystkie przypisania → zmień status członka na <em>Członek honorowy</em>.</div>
    </details>
    <details>
        <summary>Czy zawodnik może być w kilku sekcjach jednocześnie?</summary>
        <div class="faq-body">Tak — to standard dla sportów uzupełniających. Każda sekcja ma osobną składkę, obecność i statystyki.</div>
    </details>
</div>

<p class="lead">Eksport danych członka realizuje prawo do przenoszalności (art. 20 RODO). Generujemy paczkę ZIP zawierającą wszystkie dane osobowe i sportowe w formatach maszynowo czytelnych (JSON, CSV) oraz PDF dla człowieka.</p>

<h2>Krok po kroku</h2>
<ol>
    <li>Wejdź w profil członka → menu <em>⋯</em> → <strong>Eksport danych (RODO)</strong>.</li>
    <li>Wybierz zakres eksportu: <em>Dane osobowe</em>, <em>Sport (mecze, obecność, wyniki)</em>, <em>Finanse (faktury, płatności)</em>, <em>Komunikacja (otrzymane wiadomości)</em>, <em>Dokumenty</em>, <em>Wszystko</em>.</li>
    <li>Kliknij <strong>Wygeneruj paczkę</strong>. Operacja działa w tle (2-5 min dla typowego członka).</li>
    <li>Po zakończeniu pobierzesz plik ZIP zabezpieczony hasłem (hasło wyświetlane raz).</li>
    <li>Możesz wysłać paczkę bezpośrednio na zarejestrowany e-mail członka lub pobrać i przekazać osobiście.</li>
    <li>Termin realizacji wniosku — do 30 dni (art. 12 ust. 3 RODO). System zapisuje termin w panelu Compliance.</li>
</ol>

<div class="manual-callout manual-callout-tip">
    <strong><i class="bi bi-lightbulb"></i> Portal samoobsługowy.</strong> Członek może sam wygenerować eksport bez Twojej interwencji — przez portal w sekcji <em>Moje dane → RODO → Pobierz moje dane</em>. Limit: 1 eksport na miesiąc.
</div>

<h2>Najczęstsze pytania</h2>
<div class="manual-faq">
    <details>
        <summary>Co zawiera paczka?</summary>
        <div class="faq-body">JSON ze strukturą danych, CSV z transakcjami, PDF z czytelną dla człowieka kartą członka, oryginały dokumentów wgranych przez członka.</div>
    </details>
    <details>
        <summary>Czy hasło do ZIP-a jest bezpieczne?</summary>
        <div class="faq-body">Tak — 16 znaków alfanumerycznych + speciale, hash nie jest przechowywany (pokazujemy raz, przy generowaniu).</div>
    </details>
    <details>
        <summary>Czy mogę odmówić eksportu?</summary>
        <div class="faq-body">Tylko w wyjątkowych przypadkach (np. naruszenie praw osób trzecich). Odmowę musisz uzasadnić pisemnie w terminie 30 dni.</div>
    </details>
</div>

<h1>Czym jest portal opiekuna</h1>
<p class="lead">Portal opiekuna to wyspecjalizowane konto dla rodzica lub prawnego opiekuna dziecka, które trenuje w klubie. Daje Ci pełen wgląd w to, co się dzieje z Twoim podopiecznym — bez konieczności logowania na jego konto. Wszystko, co ważne: składki, obecność, wyniki, dokumenty, zgody.</p>

<h2>Dlaczego osobne konto rodzica?</h2>
<p>Dziecko poniżej 16. roku życia nie może samodzielnie wyrażać większości zgód prawnych — tak mówi RODO i polskie prawo. Klub potrzebuje Ciebie, opiekuna, żeby:</p>
<ul>
    <li><strong>Podpisać zgody</strong> — na publikację zdjęć, na badania medyczne, na uczestnictwo w zawodach.</li>
    <li><strong>Płacić składki</strong> i mieć faktury wystawione na Twoje dane.</li>
    <li><strong>Zarządzać dokumentami</strong> — wgrywać badania, oświadczenia, kopie ID.</li>
    <li><strong>Otrzymywać ważne powiadomienia</strong> — odwołania treningów, wezwania.</li>
</ul>

<p>Dziecko może (od pewnego wieku) korzystać z własnego portalu zawodnika — ale Twoje konto jest zawsze nadrzędne i widzi więcej.</p>

<h2>Co widzisz w portalu opiekuna</h2>
<ul>
    <li><strong>Lista Twoich podopiecznych</strong> — jeśli masz w klubie więcej niż jedno dziecko (rodzeństwo).</li>
    <li><strong>Profil dziecka</strong> z pełnymi danymi i statusem członkostwa.</li>
    <li><strong>Składki i faktury</strong> — zapłać online za jedno, drugie, albo za wszystkie naraz.</li>
    <li><strong>Frekwencja i wyniki</strong> — sprawdź jak dziecko trenuje i radzi sobie na zawodach.</li>
    <li><strong>Wiadomości od trenerów</strong> — bezpośredni kontakt.</li>
    <li><strong>Zgody RODO</strong> — wyrażone w imieniu dziecka.</li>
</ul>


<h2>Najczęstsze pytania</h2>
<details>
    <summary>Mam dwoje dzieci w klubie — potrzebuję dwóch kont?</summary>
    <p>Nie. Jedno konto rodzica obsługuje wszystkich Twoich podopiecznych. Po zalogowaniu wybierasz, kogo chcesz w danej chwili dotyczyć.</p>
</details>
<details>
    <summary>Mój mąż / partnerka też chce mieć dostęp</summary>
    <p>Tak, klub może utworzyć drugie konto opiekuna podpięte do tego samego dziecka. Wystarczy poprosić sekretariat.</p>
</details>
<details>
    <summary>Czy dziecko może coś samo zrobić?</summary>
    <p>Od pewnego wieku tak — np. zapisać się na trening, zobaczyć kalendarz. Jednak zgody RODO i płatności są nadal po Twojej stronie, jeśli jest niepełnoletnie.</p>
</details>

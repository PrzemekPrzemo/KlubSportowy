<h2>Cofnięcie zgody w imieniu dziecka</h2>
<p>Najczęstsza sytuacja: wcześniej zgodziłeś(aś) się, żeby klub publikował zdjęcia dziecka. Teraz dziecko mówi „nie chcę". Co robisz?</p>
<ol>
    <li><span class="manual-step-num">1</span>Wejdź w <em>Profil dziecka → Zgody</em>.</li>
    <li><span class="manual-step-num">2</span>Znajdź konkretną zgodę (np. „Publikacja wizerunku").</li>
    <li><span class="manual-step-num">3</span>Przesuń suwak na pozycję wyłączoną.</li>
    <li><span class="manual-step-num">4</span>Kliknij <strong>Zapisz</strong> i potwierdź hasłem.</li>
    <li><span class="manual-step-num">5</span>Klub ma do 30 dni na usunięcie aktywnych materiałów ze swoich kanałów (strona, social).</li>
</ol>

<div class="manual-warn">
    <strong>Ograniczenie.</strong> Cofnięcie zgody nie zmusza klubu do usunięcia materiałów już opublikowanych przed Twoim wycofaniem — np. zdjęć z gazetki sprzed roku. Klub usunie tylko aktywne miejsca (obecna strona, profile social).
</div>

<h2>Co zostaje po usunięciu konta?</h2>
<p>Nawet po wykonaniu prawa do bycia zapomnianym (art. 17), klub musi zachować pewne dane:</p>
<ul>
    <li><strong>Faktury</strong> — minimum 5 lat (prawo podatkowe).</li>
    <li><strong>Dokumenty członkowskie</strong> — zgodnie z regulaminem (zwykle 2-5 lat po wygaśnięciu).</li>
    <li><strong>Wyniki sportowe</strong> — zwykle anonimizowane (imię zastąpione kodem typu „Zawodnik_142").</li>
    <li><strong>Korespondencja prawna</strong> — np. z federacją.</li>
</ul>

<h2>Gdy dziecko skończy 16 lat</h2>
<p>System automatycznie ogranicza Twoje uprawnienia: nie możesz już cofać zgód marketingowych w imieniu dziecka (np. SMS-y o eventach) — dziecko musi je samo wyłączyć. Zachowujesz jednak pełen wgląd w dane, możliwość płacenia składek i wszystkie pozostałe prawa do osiągnięcia pełnoletności.</p>

<h2>Najczęstsze pytania</h2>
<details>
    <summary>Mam wątpliwości, czy klub działa zgodnie z prawem</summary>
    <p>Każdy klub powinien wskazać IOD (Inspektora Ochrony Danych) — kontakt w stopce ekranu i w polityce prywatności. Możesz też złożyć skargę do Prezesa UODO (Urząd Ochrony Danych Osobowych).</p>
</details>
<details>
    <summary>Dziecko chce wycofać zgodę, ja się nie zgadzam</summary>
    <p>Do 16. roku życia decydujesz Ty. Po 16-tej decyduje dziecko, ale tylko w zakresie zgód marketingowych. Klub uznaje wycofanie zgody przez Ciebie, jeśli dotyczy dziecka poniżej 16 lat.</p>
</details>
<details>
    <summary>Mój małżonek nie zgadza się z moją decyzją</summary>
    <p>Każdy z opiekunów prawnych może niezależnie wyrażać i cofać zgody. Klub uznaje wycofanie przez jednego z opiekunów za skuteczne (zasada ostrożności).</p>
</details>
<details>
    <summary>Klub nie odpowiada na wniosek o eksport / usunięcie</summary>
    <p>Klub ma maksymalnie 30 dni. Jeśli minęły bez odpowiedzi, masz prawo złożyć skargę do UODO. Najpierw jednak ponów próbę pisemnie do IOD klubu.</p>
</details>

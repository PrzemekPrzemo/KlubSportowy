<h1>RODO za niepełnoletniego</h1>
<p class="lead">Twoje dziecko jest osobą fizyczną — RODO chroni jego dane tak samo jak Twoje. Ale w przypadku dzieci są dodatkowe zasady (artykuł 8 RODO), które dotyczą szczególnie Was, rodziców. W skrócie: jako opiekun prawny <strong>decydujesz w imieniu dziecka</strong> dopóki ma poniżej 16 lat, a wybrane zgody nadal po 16. roku życia.</p>

<h2>Co mówi RODO art. 8</h2>
<p>Artykuł 8 RODO ustala próg wiekowy <strong>16 lat</strong> (Polska przyjęła ten próg w przepisach krajowych). Poniżej tego wieku:</p>
<ul>
    <li>Zgody dotyczące usług społeczeństwa informacyjnego (np. publikacja w internecie, marketing) wymagają zgody opiekuna prawnego.</li>
    <li>Klub musi zweryfikować, że osoba wyrażająca zgodę faktycznie ma władzę rodzicielską.</li>
    <li>Ty masz pełne prawo dostępu do danych dziecka, ich sprostowania, usunięcia i przenoszenia.</li>
</ul>

<p>Po 16. urodzinach dziecko może samodzielnie wyrazić wybrane zgody (marketing, profil publiczny), ale niektóre zgody prawne (np. zgoda na uczestnictwo, na badania medyczne, na zgłoszenia do federacji) nadal wymagają opiekuna do osiągnięcia pełnoletności.</p>

<h2>Co możesz zrobić jako rodzic</h2>
<p>Wszystkie prawa RODO Twojego dziecka — wykonujesz w jego imieniu z poziomu portalu opiekuna:</p>
<ul>
    <li><strong>Wyrazić / wycofać zgodę</strong> (art. 7) — np. na publikację zdjęć.</li>
    <li><strong>Sprawdzić, jakie dane klub przechowuje</strong> (art. 15) — pełen wgląd w profil.</li>
    <li><strong>Sprostować dane</strong> (art. 16) — bezpośrednio z profilu.</li>
    <li><strong>Zażądać usunięcia danych</strong> (art. 17) — wnioskiem z portalu.</li>
    <li><strong>Wyeksportować dane</strong> (art. 20) — paczka ZIP z profilem, fakturami, wynikami.</li>
    <li><strong>Wnieść sprzeciw wobec przetwarzania</strong> (art. 21) — pisemnie do IOD klubu.</li>
</ul>


<p class="lead">Badania medyczne sportowców są wymagane przepisami federacji i kodeksu sportowego. ClubDesk pomaga śledzić daty ważności badań i automatycznie powiadamia o nadchodzącym wygaśnięciu (30/14/7 dni przed).</p>

<h2>Krok po kroku</h2>
<ol>
    <li>W profilu zawodnika wejdź w zakładkę <strong>Badania medyczne</strong>.</li>
    <li>Kliknij <em>+ Dodaj badanie</em>.</li>
    <li>Wybierz typ: <em>Badanie sportowo-lekarskie</em>, <em>EKG</em>, <em>Badanie kardiologa</em>, <em>Doping</em>, inne.</li>
    <li>Wpisz datę wykonania, datę ważności, lekarza/przychodnię.</li>
    <li>Wgraj skan/zdjęcie zaświadczenia (PDF/JPG).</li>
    <li>System automatycznie ustawi alerty 30/14/7 dni przed wygaśnięciem.</li>
</ol>

<div class="manual-callout manual-callout-warn">
    <strong><i class="bi bi-exclamation-triangle"></i> Blokada startu.</strong> Zawodnik z nieaktualnymi badaniami jest automatycznie blokowany w listach startowych turniejów. Trener widzi czerwoną flagę w jego profilu.
</div>

<h2>Najczęstsze pytania</h2>
<div class="manual-faq">
    <details>
        <summary>Czy zawodnik widzi swoje badania?</summary>
        <div class="faq-body">Tak — w portalu, w sekcji <em>Moje dokumenty → Badania</em>. Może też samodzielnie wgrać nowe.</div>
    </details>
    <details>
        <summary>Czy lekarz klubowy ma osobny dostęp?</summary>
        <div class="faq-body">Tak — rola <em>lekarz</em> widzi badania wszystkich członków, ale nie widzi danych finansowych.</div>
    </details>
    <details>
        <summary>Co z RODO?</summary>
        <div class="faq-body">Dane medyczne to dane szczególnej kategorii (art. 9 RODO). System stosuje szyfrowanie at-rest i pełny audit log dostępu.</div>
    </details>
</div>

<p class="lead">Certyfikacje trenerów (kursy, licencje, kategorie szkoleniowe — np. UEFA B, UEFA A, PZPN C/B/A/Pro) wymagają ewidencji w klubie. ClubDesk śledzi terminy ważności i wymaga aktualnej certyfikacji do prowadzenia treningów na danym poziomie.</p>

<h2>Krok po kroku</h2>
<ol>
    <li>W profilu trenera wejdź w zakładkę <strong>Certyfikacje</strong>.</li>
    <li>Dodaj certyfikację: nazwa kursu/licencji, organizator (PZPN, PZPS, etc.), data uzyskania, data ważności (lub bezterminowo).</li>
    <li>Wgraj skan certyfikatu.</li>
    <li>Wpisz kategorię (poziom uprawnień — np. „UEFA B / 1.+2. liga juniorów").</li>
    <li>System weryfikuje uprawnienia trenera przy przypisywaniu do sekcji.</li>
</ol>

<div class="manual-callout manual-callout-tip">
    <strong><i class="bi bi-lightbulb"></i> CPD — Continuous Professional Development.</strong> Włącz <em>Punkty CPD</em> aby śledzić rozwój trenerów (uczestnictwo w konferencjach, kursach, webinarach) — wymagane przez niektóre federacje.
</div>

<h2>Najczęstsze pytania</h2>
<div class="manual-faq">
    <details>
        <summary>Co jeśli certyfikacja wygasa?</summary>
        <div class="faq-body">System powiadamia 60/30/14 dni przed wygaśnięciem i blokuje przypisywanie do nowych sekcji.</div>
    </details>
    <details>
        <summary>Czy mogę dodać licencje zagraniczne?</summary>
        <div class="faq-body">Tak — wybierz <em>Organizator: Inny / Zagraniczny</em> i wpisz pełną nazwę.</div>
    </details>
    <details>
        <summary>Kto może edytować certyfikacje?</summary>
        <div class="faq-body">Sam trener (wgrywa swoje), administrator klubu (wszystkie), dyrektor sportowy.</div>
    </details>
</div>

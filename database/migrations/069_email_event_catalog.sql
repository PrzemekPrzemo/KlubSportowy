-- 069: Katalog dostepnych eventow ktore moga wyzwalac email per klub.
-- Pozwala administratorom zobaczyc liste wszystkich dostepnych
-- "zdarzen" (welcome, fee_reminder, training_cancelled, ...) z opisem,
-- domyslnym subject/body i lista dostepnych placeholderow.

SET foreign_key_checks = 0;

CREATE TABLE IF NOT EXISTS `email_event_catalog` (
    `id` INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    `code` VARCHAR(60) NOT NULL UNIQUE,
    `name` VARCHAR(120) NOT NULL,
    `description` VARCHAR(500) NULL,
    `category` VARCHAR(40) NOT NULL DEFAULT 'general',
    `default_subject` VARCHAR(200) NULL,
    `default_body` TEXT NULL,
    `available_variables` JSON NULL COMMENT 'Lista placeholderow, np. ["member.first_name","fee.amount"]',
    `is_active` TINYINT(1) NOT NULL DEFAULT 1,
    `sort_order` SMALLINT UNSIGNED NOT NULL DEFAULT 0,
    KEY `idx_eec_category` (`category`, `sort_order`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci
  COMMENT='Katalog eventow ktore moga wyzwalac email';

